"""Generate messy preset datasets for the Cleaning workbench.

Each preset deliberately contains the four issue families from Section 5.2:

* missing values (empty strings / None)
* duplicate rows (exact and near-duplicates sharing an ID)
* inconsistent categories (case, whitespace, synonyms)
* outliers (impossible or extreme numeric values)

Presets are bundled as ``window.CleaningPresets`` in a classic script so the
tool works under ``file://`` (same pattern as the sampling/annotation tools).

Run:
    python make_presets.py
"""

from __future__ import annotations

import json
import random
from pathlib import Path


def customer_orders(rng: random.Random) -> dict:
    """Retail orders with all four issue families baked in."""
    cities = ["Singapore", "singapore", " Singapore", "Kuala Lumpur", "kuala lumpur", "Jakarta", "Bangkok"]
    channels = ["online", "Online", "ONLINE", "retail", "Retail", "phone"]
    rows = []
    for i in range(1, 121):
        oid = f"O{i:04d}"
        age = rng.randint(18, 70)
        amount = round(rng.uniform(8, 240), 2)
        rows.append(
            {
                "order_id": oid,
                "customer_age": age if rng.random() > 0.08 else "",
                "city": rng.choice(cities),
                "channel": rng.choice(channels),
                "amount": amount if rng.random() > 0.06 else "",
                "rating": rng.randint(1, 5),
            }
        )
    # outliers: impossible age, extreme amount
    rows[10]["customer_age"] = 214
    rows[43]["customer_age"] = -3
    rows[27]["amount"] = 99999.0
    rows[88]["amount"] = 78500.5
    # exact duplicates
    rows.append(dict(rows[5]))
    rows.append(dict(rows[17]))
    rows.append(dict(rows[17]))
    # near-duplicate: same order id, one field differs
    near = dict(rows[30])
    near["rating"] = 4 if rows[30]["rating"] != 4 else 3
    rows.append(near)
    rng.shuffle(rows)
    return {
        "name": "customer-orders",
        "description": "Retail orders: missing ages/amounts, city and channel spelling variants, "
        "impossible ages, extreme amounts, duplicated orders.",
        "id_column": "order_id",
        "rows": rows,
    }


def sensor_log(rng: random.Random) -> dict:
    """IoT sensor log: gaps, unit inconsistency, stuck/extreme readings."""
    statuses = ["ok", "OK", "Ok ", "warn", "WARN", "fail"]
    rows = []
    for i in range(1, 101):
        temp = round(rng.gauss(24, 3), 1)
        rows.append(
            {
                "reading_id": f"R{i:04d}",
                "sensor": rng.choice(["s-01", "s-02", "s-03", "S-01", "s-1"]),
                "temp_c": temp if rng.random() > 0.1 else "",
                "humidity_pct": round(rng.uniform(35, 85), 1) if rng.random() > 0.07 else "",
                "status": rng.choice(statuses),
            }
        )
    rows[15]["temp_c"] = 999.9  # sensor glitch
    rows[52]["temp_c"] = -80.0
    rows[70]["humidity_pct"] = 480.0
    rows.append(dict(rows[8]))
    rows.append(dict(rows[8]))
    rng.shuffle(rows)
    return {
        "name": "sensor-log",
        "description": "IoT readings: missing temperatures/humidity, sensor-name variants, "
        "status case noise, glitch readings (999.9, -80), duplicated rows.",
        "id_column": "reading_id",
        "rows": rows,
    }


def main() -> None:
    rng = random.Random(42)
    presets = {}
    for build in (customer_orders, sensor_log):
        data = build(rng)
        presets[data["name"]] = data

    out = Path(__file__).with_name("presets-bundle.js")
    payload = json.dumps(presets, ensure_ascii=False, separators=(",", ":"))
    out.write_text(
        "/* Auto-generated by make_presets.py — messy datasets for the Cleaning workbench.\n"
        " * Bundled as a classic script so the tool works under file://. */\n"
        "window.CleaningPresets = " + payload + ";\n",
        encoding="utf-8",
    )
    print(f"wrote {out} ({out.stat().st_size // 1024} KB, {len(presets)} presets)")


if __name__ == "__main__":
    main()
