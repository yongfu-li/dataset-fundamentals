/* Classic script — export metadata scorecard JSON and Markdown. */
(function (global) {
  "use strict";
  const MetadataCheckerLib = global.MetadataCheckerLib || (global.MetadataCheckerLib = {});

  function download(filename, text, mime) {
    const blob = new Blob([text], { type: mime || "text/plain;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    setTimeout(function () {
      URL.revokeObjectURL(url);
    }, 500);
  }

  function buildPayload(source, card, report) {
    return {
      tool: "Metadata completeness checker",
      generated: new Date().toISOString(),
      book_anchors: ["§8.2 Key Components of Dataset Documentation", "§8.3 Creating Effective Documentation"],
      source: source,
      dataset_name: card.dataset && card.dataset.name ? card.dataset.name : "",
      score_pct: report.score_pct,
      grade: report.grade,
      earned: report.earned,
      total: report.total,
      passed: report.passed,
      check_count: report.check_count,
      categories: report.categories.map(function (c) {
        return {
          id: c.id,
          title: c.title,
          section: c.section,
          pct: c.pct,
          earned: c.earned,
          total: c.total,
        };
      }),
      results: report.results,
      remediation: report.remediation,
    };
  }

  function renderMarkdown(source, card, report) {
    const name = (card.dataset && card.dataset.name) || source;
    let md = "# Metadata completeness scorecard\n\n";
    md += "**Dataset:** " + name + "  \n";
    md += "**Source:** " + source + "  \n";
    md += "**Score:** " + report.score_pct + "% (" + report.grade + ")  \n";
    md += "**Checks passed:** " + report.passed + " / " + report.check_count + "  \n\n";
    md += "---\n\n## Summary by category (§8.2)\n\n";
    md += "| Category | Score | Section |\n|----------|-------|--------|\n";
    report.categories.forEach(function (c) {
      md += "| " + c.title + " | " + c.pct + "% | " + c.section + " |\n";
    });
    md += "\n## Checklist\n\n";
    report.categories.forEach(function (cat) {
      md += "### " + cat.title + "\n\n";
      cat.items.forEach(function (item) {
        md += "- [" + (item.pass ? "x" : " ") + "] **" + item.label + "**";
        if (!item.pass) md += " — _" + item.hint + "_";
        md += "\n";
      });
      md += "\n";
    });
    if (report.remediation.length) {
      md += "## Remediation (priority fixes)\n\n";
      report.remediation.forEach(function (r, i) {
        md += i + 1 + ". **" + r.label + "** — " + r.hint + "\n";
      });
      md += "\n";
    }
    md += "---\n\n_Generated by the Metadata completeness checker (Dataset Fundamentals, §8.2–8.3)._ \n";
    return md;
  }

  function downloadJson(source, card, report) {
    download("metadata-scorecard.json", JSON.stringify(buildPayload(source, card, report), null, 2), "application/json");
  }

  function downloadMarkdown(source, card, report) {
    download("metadata-scorecard.md", renderMarkdown(source, card, report), "text/markdown;charset=utf-8");
  }

  MetadataCheckerLib.buildPayload = buildPayload;
  MetadataCheckerLib.renderMarkdown = renderMarkdown;
  MetadataCheckerLib.downloadJson = downloadJson;
  MetadataCheckerLib.downloadMarkdown = downloadMarkdown;
})(typeof window !== "undefined" ? window : globalThis);
