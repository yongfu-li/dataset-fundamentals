def main() -> None:
    bag = {
        "flat-customers": {
            "id": "flat-customers",
            "name": "flat-customers",
            "title": "Flat customers (structured)",
            "description": "Fixed columns — CSV and JSON describe the same table (Ch.1 §1.2 structured).",
            "bookAnchors": ["§1.2", "§1.2.5"],
            "teachingFocus": "structured",
            "structureHint": "structured",
            "rows": flat_customers(),
        },
        "housing-nested": {
            "id": "housing-nested",
            "name": "housing-nested",
            "title": "Housing listings (nested)",
            "description": "Nested address + amenities array — semi-structured JSON (eg:1.4 motif).",
            "bookAnchors": ["§1.2", "eg:1.4"],
            "teachingFocus": "semi-structured",
            "structureHint": "semi-structured",
            "rows": housing_nested(),
        },
        "orders-nested": {
            "id": "orders-nested",
            "name": "orders-nested",
            "title": "Orders with line items",
            "description": "One order, many items — flattening to CSV expands rows or stringifies arrays.",
            "bookAnchors": ["§1.2", "§1.2.5"],
            "teachingFocus": "flatten-tradeoff",
            "structureHint": "semi-structured",
            "rows": orders_nested(),
        },
    }
    js = (
        "/* Generated by make_presets.py — do not edit by hand. */\n"
        "(function (global) {\n"
        "  global.SchemaPresets = "
        + json.dumps(bag, indent=2)
        + ";\n"
        "})(typeof window !== 'undefined' ? window : globalThis);\n"
    )
    OUT.write_text(js, encoding="utf-8")
    print(f"Wrote {OUT} ({len(bag)} presets)")


if __name__ == "__main__":
    main()
