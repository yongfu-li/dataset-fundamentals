/* Classic script — export representation gap JSON and Markdown. */
(function (global) {
  "use strict";
  const RepresentationLib = global.RepresentationLib || (global.RepresentationLib = {});

  function download(filename, text, mime) {
    const blob = new Blob([text], { type: mime || "text/plain;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    setTimeout(function () {
      URL.revokeObjectURL(url);
    }, 500);
  }

  function buildPayload(source, mapping, gapReport, perfReport, description) {
    return {
      tool: "Representation bias visualizer",
      generated: new Date().toISOString(),
      book_anchors: [
        "§7.2.1 Sampling Bias: Unequal Representation of Groups",
        "§7.3 Detecting Bias — distribution comparison",
      ],
      source: source,
      description: description || "",
      mapping: mapping,
      total_rows: gapReport.total,
      skipped_rows: gapReport.skipped,
      max_abs_gap_pp: gapReport.maxAbsGapPp,
      flag_threshold_pp: gapReport.flagThresholdPp,
      population: gapReport.population,
      groups: gapReport.items,
      performance: perfReport || null,
    };
  }

  function renderMarkdown(source, mapping, gapReport, perfReport) {
    let md = "# Representation gap summary\n\n";
    md += "**Source:** " + source + "  \n";
    md += "**Group column:** " + (mapping.group || "") + "  \n";
    md += "**Rows used:** " + gapReport.total + "  \n";
    md += "**Max |gap|:** " + gapReport.maxAbsGapPp + " pp  \n\n";
    md += "---\n\n## Population vs dataset\n\n";
    md += "| Group | Population % | Dataset % | Gap (pp) | Ratio | Flag |\n";
    md += "|-------|--------------|-----------|----------|-------|------|\n";
    gapReport.items.forEach(function (it) {
      const ratio = it.representationRatio == null ? "—" : String(it.representationRatio);
      md +=
        "| " +
        it.group +
        " | " +
        (it.populationShare * 100).toFixed(1) +
        " | " +
        (it.datasetShare * 100).toFixed(1) +
        " | " +
        it.gapPp +
        " | " +
        ratio +
        " | " +
        (it.flagged ? "yes" : "") +
        " |\n";
    });
    if (perfReport && perfReport.groups) {
      md += "\n## Per-group accuracy (threshold " + perfReport.threshold + ")\n\n";
      md += "| Group | n | Accuracy | TPR |\n|-------|---|----------|-----|\n";
      perfReport.groups.forEach(function (g) {
        md +=
          "| " +
          g.group +
          " | " +
          g.n +
          " | " +
          (g.accuracy * 100).toFixed(1) +
          "% | " +
          (g.tpr * 100).toFixed(1) +
          "% |\n";
      });
      md += "\nOverall accuracy: " + (perfReport.overallAccuracy * 100).toFixed(1) + "%\n";
    }
    md +=
      "\n---\n\n_Generated by the Representation bias visualizer (Dataset Fundamentals, §7.2–7.3)._\n";
    return md;
  }

  function downloadJson(source, mapping, gapReport, perfReport, description) {
    download(
      "representation-gap.json",
      JSON.stringify(buildPayload(source, mapping, gapReport, perfReport, description), null, 2),
      "application/json"
    );
  }

  function downloadMarkdown(source, mapping, gapReport, perfReport) {
    download("representation-gap.md", renderMarkdown(source, mapping, gapReport, perfReport), "text/markdown;charset=utf-8");
  }

  RepresentationLib.buildPayload = buildPayload;
  RepresentationLib.renderMarkdown = renderMarkdown;
  RepresentationLib.downloadJson = downloadJson;
  RepresentationLib.downloadMarkdown = downloadMarkdown;
})(typeof window !== "undefined" ? window : globalThis);
