#!/usr/bin/env python3
"""Generate presets for Class imbalance explorer."""
from __future__ import annotations

import json
import random
from pathlib import Path

OUT = Path(__file__).resolve().parent / "presets-bundle.js"


def _row(i: int, amount: float, risk: float, label: int) -> dict:
    return {
        "id": f"R{i:04d}",
        "amount": round(amount, 2),
        "risk_score": round(risk, 3),
        "label": label,
    }


def _gen(n: int, minority_rate: float, seed: int, pos_mean_risk: float, overlap: float = 0.2) -> list[dict]:
    rng = random.Random(seed)
    n_pos = max(1, int(round(n * minority_rate)))
    n_neg = n - n_pos
    rows: list[dict] = []
    idx = 1
    for _ in range(n_neg):
        amount = rng.gauss(90, 30)
        risk = abs(rng.gauss(0.32, 0.18))
        # occasional noisy high-risk negatives
        if rng.random() < overlap:
            risk = abs(rng.gauss(0.55, 0.2))
        rows.append(_row(idx, max(5, amount), min(0.95, risk), 0))
        idx += 1
    for _ in range(n_pos):
        amount = rng.gauss(110, 35)
        risk = abs(rng.gauss(pos_mean_risk, 0.22))
        # many positives look like typical negatives (hard minority)
        if rng.random() < overlap + 0.15:
            risk = abs(rng.gauss(0.34, 0.15))
            amount = rng.gauss(95, 28)
        rows.append(_row(idx, max(5, amount), min(0.99, risk), 1))
        idx += 1
    rng.shuffle(rows)
    for i, r in enumerate(rows, start=1):
        r["id"] = f"R{i:04d}"
    return rows


def fraud_rare() -> dict:
    rows = _gen(400, 0.025, seed=7, pos_mean_risk=0.48, overlap=0.25)
    return {
        "id": "fraud-rare",
        "name": "fraud-rare",
        "title": "Rare fraud (~2.5%)",
        "description": "Extreme imbalance: a majority-class predictor looks accurate but misses fraud.",
        "bookAnchors": ["§5.2", "sec:5.2.5"],
        "teachingFocus": "accuracy-trap",
        "labelColumn": "label",
        "featureColumns": ["amount", "risk_score"],
        "positiveLabel": "1",
        "rows": rows,
    }


def churn_mild() -> dict:
    rows = _gen(300, 0.18, seed=11, pos_mean_risk=0.52, overlap=0.12)
    return {
        "id": "churn-mild",
        "name": "churn-mild",
        "title": "Mild churn (~18%)",
        "description": "Moderate skew where resampling and class weights still change recall.",
        "bookAnchors": ["§5.2", "sec:5.2.5"],
        "teachingFocus": "mild-imbalance",
        "labelColumn": "label",
        "featureColumns": ["amount", "risk_score"],
        "positiveLabel": "1",
        "rows": rows,
    }


def balanced_demo() -> dict:
    rows = _gen(280, 0.48, seed=19, pos_mean_risk=0.55, overlap=0.08)
    return {
        "id": "balanced-demo",
        "name": "balanced-demo",
        "title": "Nearly balanced (~48%)",
        "description": "Baseline where accuracy and F1 move together—contrast with fraud-rare.",
        "bookAnchors": ["§5.2"],
        "teachingFocus": "balanced-control",
        "labelColumn": "label",
        "featureColumns": ["amount", "risk_score"],
        "positiveLabel": "1",
        "rows": rows,
    }


def main() -> None:
    presets = {
        "fraud-rare": fraud_rare(),
        "churn-mild": churn_mild(),
        "balanced-demo": balanced_demo(),
    }
    # tiny sanity: rates
    for p in presets.values():
        labels = [int(r["label"]) for r in p["rows"]]
        rate = sum(labels) / len(labels)
        p["meta"] = {"n": len(labels), "positiveRate": round(rate, 4)}
    body = (
        "/* Auto-generated by make_presets.py — do not edit by hand. */\n"
        "(function (global) {\n"
        '  "use strict";\n'
        f"  global.ImbalancePresets = {json.dumps(presets, indent=2)};\n"
        "})(typeof window !== 'undefined' ? window : globalThis);\n"
    )
    OUT.write_text(body, encoding="utf-8")
    print(f"Wrote {OUT} ({len(presets)} presets)")


if __name__ == "__main__":
    main()
